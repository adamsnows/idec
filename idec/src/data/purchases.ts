// Função para gerar um número aleatório entre min e max (incluindo min e excluindo max)
export const getRandomNumber = (min: number, max: number): number =>
  Math.floor(Math.random() * (max - min) + min);

// Função para gerar uma descrição de compra aleatória
export const getRandomPurchaseDescription = (): string => {
  const index = getRandomNumber(0, purchaseDescription.length);
  return purchaseDescription[index];
};

// Função para gerar um nome completo aleatório
export const getRandomFullName = (): string => {
  const name = names[getRandomNumber(0, names.length)];
  const surname = surnames[getRandomNumber(0, surnames.length)];
  return `${name} ${surname}`;
};

// Função para gerar um array de objetos com nomes, sobrenomes e descrições de compra
export const generatePurchaseData = (quantity: number): PurchaseData[] => {
  const data: PurchaseData[] = [];
  for (let i = 0; i < quantity; i++) {
    const fullName = getRandomFullName();
    const purchaseDescription = getRandomPurchaseDescription();
    data.push({ fullName, purchaseDescription });
  }
  return data;
};

// Gerar dados de compra para 500 clientes
export const purchaseData: PurchaseData[] = generatePurchaseData(500);
